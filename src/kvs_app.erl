-module(kvs_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    Dispatch = cowboy_router:compile([
        {'_', [{'_', rest_handler, []}]}
    ]),

    cowboy:start_http(kvs_http_listener, 100, [{port, 8080}],
        [{env, [{dispatch, Dispatch}]}]
    ),

    kvs_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

